/*
IF e ELSE -> Controlador de Fluxo

If(SE)
Else(SE NAO)

Operadores de Comparação

> Maior que
< Menor que
>= Maior ou igual
<= Menor ou igual
== Igual (Comparando somente o valor)
=== Identico (Comparando o valor e o tipo)
*/

const notaDoAluno = 4;

if (notaDoAluno > 5) {
  // SE
  console.log("Aluno Aprovado");
} else {
  // SE NÃO
  console.log("Aluno Reprovado");
}
